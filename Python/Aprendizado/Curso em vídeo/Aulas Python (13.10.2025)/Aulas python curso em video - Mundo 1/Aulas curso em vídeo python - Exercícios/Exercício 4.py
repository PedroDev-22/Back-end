V=input('Digite um valor')
print(type(V))
print(f'"{V}" É \033[31;44malfabético\033[m?',V.isalpha())
print(f'"{V}" É \033[32;41mnúmerico\033[m?',V.isnumeric())
print(f'"{V}" Está em \033[33;42maiusculo\033[m?',V.isupper())
print(f'"{V}" Está em \033[34;43mminusculo\033[m?',V.islower())
print(f'"{V}" É \033[35;44mAlphanúmerico\033[m?',V.isalnum())
print(f'"{V}" É só \033[36;45mespaço\033[m?',V.isspace())
print(f'"{V}" Está \033[37;46mCapitalizada\033[m?',V.istitle())
